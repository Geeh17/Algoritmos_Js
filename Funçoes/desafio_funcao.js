// soma, subtração, multiplicação e divição

function soma (num1, num2){
    console.log(num1 + num2);
}
soma(3,4);
soma(4,4);

function subtracao (num1, num2){
    console.log(num1 - num2);
}
subtracao(15,6);
subtracao(5,6);

function multiplicacao (num1, num2){
    console.log(num1 * num2);
}
multiplicacao(2,6);
multiplicacao(5,6);

function divicao (num1, num2){
    console.log(num1 / num2);
}
divicao(6,6);
divicao(36,6);